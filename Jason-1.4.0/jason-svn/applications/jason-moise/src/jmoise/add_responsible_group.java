package jmoise;

import jason.asSemantics.TransitionSystem;
import jason.asSemantics.Unifier;
import jason.asSyntax.Term;

/** 

<p>Organisational action: <b><code>jmoise.add_responsible_group( SchId, GrId )</code></b>: 
   used by an agent to assign a new responsible group for the execution of the scheme. 
   SchId is the identification of a scheme instance and GrId a group instance. 
   
<p>Conditions to succeed:
<ul>
<li>the agent performing the action is the owner of GrId.</li>
</ul>

<p>Consequences:
<ul>
<li>the agents playing roles in the group becomes permitted/obliged to the missions in the scheme.</li>
</ul>

<p>Examples:
<ul>
<li> <code>jmoise.add_responsible_group(wp0, g1)</code>: 
     add the group g1 as responsible for scheme wp0.</li>
</ul>
 
@see jmoise.create_scheme
@see jmoise.remove_scheme 
@see <a href="http://moise.sourceforge.net/doc/tutorial.pdf">Moise+ Tutorial, chapter for J-Moise+</a> 
 
@author Jomi 
*/
public class add_responsible_group extends MoiseBaseIA {

    @Override
    public Object execute(TransitionSystem ts, Unifier un, Term[] args) throws Exception {
        return super.execute(ts,un,args);
    }
}
